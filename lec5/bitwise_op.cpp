#include<bits/stdc++.h>
using namespace std;

int main()
{
    int a=4,b=6;
    cout<<(a&b)<<endl; //output will be 4
    cout<<(a|b)<<endl; //output will be 6
    cout<<(~a)<<endl; //output will be -5
    cout<<(a^b)<<endl; //output will be 2
    cout<<endl;

    cout<<(3<<2)<<endl; //output will be 12 (left shift)
    cout<<(17>>2)<<endl; //output will be 4 (right shift)
}